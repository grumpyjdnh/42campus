#include <stdio.h>

int	ft_recursive_power(int nb, int power);

int	ft_atoi(char *str)
{
	int	i;
	int	sign;
	int	num;

	i = 0;
	sign = 1;
	num = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\r')
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = -sign;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10 + (str[i] - '0');
		i++;
	}
	return (sign * num);
}

int	main(int argc, char **argv)
{
	if (argc != 3)
		return (0);
	printf("%d", ft_recursive_power(ft_atoi(argv[1]), ft_atoi(argv[2])));
	return (0);
}
